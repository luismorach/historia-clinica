    private void registraUser() {
        if (usuarios.Opciones.verificaUsuario(this.txtUser.getText()).equals(this.txtUser.getText())) {
            ErrorAlert a = new ErrorAlert(null, true);
            a.msj1.setText("El nombre de usuario que intenta");
            a.msj2.setText("registrar ya existe.");
            a.msj3.setVisible(false);
            a.setVisible(true);
        } else {
            usuarios.Sentencias s = new Sentencias();

            s.setId(Integer.parseInt(this.lblID.getText()));
            s.setNombre(this.txtNombre.getText());
            s.setUsuario(this.txtUser.getText());
            s.setPassword(this.txtPass.getText());
            s.setTipo_usuario(this.comboTipo.getSelectedItem().toString());

            if (usuarios.Opciones.registrar(s)) {

                usuarios.Opciones.listar("");
                usuarios.Opciones.selecionaFila(this.lblID.getText());
                limpiarCampos();
                usuarios.Opciones.extraerIDMax();

                SuccessAlert a = new SuccessAlert(null, true);
                a.msj1.setText("Usuario registrado con exito.");
                a.msj2.setVisible(false);
                a.msj3.setVisible(false);
                a.setVisible(true);
            }
        }
    }

    private void guardarUser() {
        if (usuarios.Opciones.verificaUsuario(this.txtUser.getText()).equals(this.txtUser.getText())
                && !this.txtUser.getText().equals(this.tablaUsuarios.getValueAt(
                        this.tablaUsuarios.getSelectedRow(), 2))) {
            ErrorAlert a = new ErrorAlert(null, true);
            a.msj1.setText("El nombre de usuario que intenta");
            a.msj2.setText("guardar ya existe.");
            a.msj3.setVisible(false);
            a.setVisible(true);
        } else {
            usuarios.Sentencias s = new Sentencias();

            s.setId(Integer.parseInt(this.lblID.getText()));
            s.setNombre(this.txtNombre.getText());
            s.setUsuario(this.txtUser.getText());
            s.setPassword(this.txtPass.getText());
            s.setTipo_usuario(this.comboTipo.getSelectedItem().toString());

            if (usuarios.Opciones.actualizar(s)) {

                usuarios.Opciones.listar("");
                usuarios.Opciones.selecionaFila(this.lblID.getText());
                limpiarCampos();
                usuarios.Opciones.extraerIDMax();

                SuccessAlert a = new SuccessAlert(null, true);
                a.msj1.setText("Usuario actualizado con exito.");
                a.msj2.setVisible(false);
                a.msj3.setVisible(false);
                a.setVisible(true);
            }
        }
    }

    private void limpiarCampos() {
        this.lblUserActivo.setVisible(false);
        this.lblIDActivo.setVisible(false);
        this.lblTipo_us.setVisible(false);

        this.txtNombre.requestFocus();
        this.txtNombre.setNextFocusableComponent(this.txtUser);
        this.txtUser.setNextFocusableComponent(this.txtPass);
        this.txtPass.setNextFocusableComponent(this.comboTipo);

        this.lblID.setVisible(false);

        this.txtNombre.setText("");
        this.txtUser.setText("");
        this.txtPass.setText("");
        this.comboTipo.setSelectedIndex(0);
        this.btnCancelar.setVisible(false);
        usuarios.Opciones.extraerIDMax();
        this.btnRegistrar.setText("REGISTRAR");
    }
