    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (this.txtUser.getText().isEmpty() || String.valueOf(this.txtPass.getPassword()).isEmpty()
                || this.txtNombre.getText().isEmpty()) {

            this.lblInfo.setText("¡Todos los campos son requeridos!");
            this.txtNombre.requestFocus();
        } else {
            boolean camposCorrectos = true;

            if (String.valueOf(this.txtPass.getPassword()).length() < 8) {
                this.lblInfo.setText("¡Debés ingresar 8 caracteres como mínimo!");
                camposCorrectos = false;
                this.txtPass.requestFocus();
            }

            if (clic == 1) {
                rSPanelsSlider1.slidPanel(5, pnlCargando, RSPanelsSlider.direct.Right);
                this.btnAtras.setVisible(false);
                this.btnAceptar.setVisible(false);

                new Thread(new Runnable() {
                    @Override
                    public void run() {
                        try {
                            Thread.sleep(8000);
                            registraUser();
                        } catch (InterruptedException ex) {
                            Logger.getLogger(Configuracion.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }).start();
            }

            if (camposCorrectos && clic == 0) {
                rSPanelsSlider1.slidPanel(5, pnlVerificar, RSPanelsSlider.direct.Right);
                this.btnAtras.setVisible(true);
                this.btnAceptar.setText("ACEPTAR");
                this.lblUs.setText(this.txtUser.getText());
                this.lblPass.setText(String.valueOf(this.txtPass.getPassword()));
                this.lblNombre.setText(this.txtNombre.getText());
                clic++;
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

