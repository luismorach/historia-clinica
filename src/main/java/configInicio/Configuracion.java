    private void registraUser() {
        usuarios.Sentencias s = new usuarios.Sentencias();

        //s.setId(1);
        s.setNombre(this.txtNombre.getText());
        s.setUsuario(this.txtUser.getText());
        s.setPassword(String.valueOf(this.txtPass.getPassword()));
        s.setTipo_usuario("SUPER ADMIN");

        if (usuarios.Opciones.registrar(s)) {           
            new splash.SplashScreen().setVisible(true);
            DesktopNotify.showDesktopMessage("CONFIGURAR USUARIO SUPER ADMIN...", "El usuario SUPER ADMIN fue configurado con exito. Ahora ya "
                    + "puedes iniciar sesión en el sistema.", DesktopNotify.INFORMATION, 8000);
            this.dispose();
        }
    }
